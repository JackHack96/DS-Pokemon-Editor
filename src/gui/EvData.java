package gui;

public class EvData {
	private int type, specific, target;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getSpecific() {
		return specific;
	}

	public void setSpecific(int specific) {
		this.specific = specific;
	}

	public int getTarget() {
		return target;
	}

	public void setTarget(int target) {
		this.target = target;
	}
}